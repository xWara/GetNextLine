char	*ft_strrchr(char *s, int c)
{
	int i;

	i = 0;
	while (*s != '\0')
	{
		s++;
		i++;
	}
	while (*s != c && i != 0)
	{
		s--;
		i--;
	}
	if (*s == c)
		return (s);
	else
		return (0);
}
