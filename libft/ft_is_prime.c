#include "libft.h"

int		ft_is_prime(int nb)
{
	int j;
	int k;

	j = 2;
	k = 0;
	if (nb <= 1)
		return (0);
	while (j < nb)
	{
		if (nb % j == 0)
			return (0);
		j++;
	}
	return (1);
}
